     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        
        $paint = Paint::find($id);
        $paint->units_available = $request['units_available'];
        $paint->save();

        if($request['units_available'] <= $paint->threshold){
            \Mail::to('operations@roofvents.com')->send(new InventoryLowWarning($paint));
        }

        return redirect('/inventory');
    }

